# find the input base days
day_no = int(input("Enter the dayNumber"))
if day_no ==0:
    print("Monday")
elif day_no ==1:
    print("Tuesday")
elif day_no == 2:
    print("Wednesday")
elif day_no == 3:
    print("Thursday")
elif day_no == 4:
    print("Friday")
elif day_no == 5:
    print("Saturday")
else:
    print("Sunday")
